// Helper Functions
export function getPlacesByCity(cityId) {
    return places[cityId] || places.bangalore;
}

export function getPlaceById(cityId, placeId) {
    const cityPlaces = getPlacesByCity(cityId);
    return cityPlaces.find(p => p.id === placeId);
}

export function getPlacesByCategory(cityId, category) {
    const cityPlaces = getPlacesByCity(cityId);
    if (category === 'all') return cityPlaces;
    return cityPlaces.filter(p => p.category === category);
}

export function getFreeEntryPlaces(cityId) {
    const cityPlaces = getPlacesByCity(cityId);
    return cityPlaces.filter(p => p.entryCost === 0);
}
